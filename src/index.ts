// Get the path to icon files
function getIconPath(): string {
  const iconName = "trayIcon.png";
  // Assets are copied to 'assets' folder in webpack output
  return path.join(__dirname, "assets", iconName);
}

function createTrayIcon(): Electron.NativeImage {
  const iconPath = getIconPath();
  const image = nativeImage.createFromPath(iconPath);

  if (image.isEmpty()) {
    console.error(`[StayFree] ERROR: Icon not found at: ${iconPath}`);
  }

  // Full-color icon (cream tile + ink bars) — must NOT be a template image,
  // or macOS would flatten it to monochrome menu-bar tint.
  image.setTemplateImage(false);

  return image;
}

function updateTrayState(state: AppState): void {
  currentState = state;
  if (tray) {
    tray.setImage(createTrayIcon());
    tray.setToolTip(
      state === "idle"
        ? `StayFree - Ready (Hold ${holdKeyLabel} to dictate)`
        : state === "recording"
          ? "StayFree - Recording..."
          : "StayFree - Processing...",
    );
  }
}

// Fixed bounds for the widget window: centered horizontally, bottom edge flush
// with the bottom of the work area (the dock top). The pill inside is anchored to
// the bottom via CSS padding, so it visually sits ~8px above the dock and grows
// upward as it morphs — the native window itself never moves or resizes.
function getWidgetWindowBounds(): Electron.Rectangle {
  const display = screen.getPrimaryDisplay();
  const workArea = display.workArea;
  const x = Math.round(
    workArea.x + (workArea.width - WIDGET_WINDOW_WIDTH) / 2,
  );
  const y = Math.round(
    workArea.y + workArea.height - WIDGET_WINDOW_HEIGHT,
  );
  return {
    x,
    y,
    width: WIDGET_WINDOW_WIDTH,
    height: WIDGET_WINDOW_HEIGHT,
  };
}

function sendWidgetState(state: WidgetUiState): void {
  if (!widgetWindow || widgetWindow.isDestroyed()) return;
  // No native resize — the renderer morphs the pill purely in CSS.
  widgetWindow.webContents.send("widget-state", state);
}

function showErrorBubble(payload: WidgetErrorPayload | string): void {
  const normalizedPayload: WidgetErrorPayload =
    typeof payload === "string"
      ? {
          code: "SERVER_ERROR",
          message: payload,
        }
      : payload;

  // Clear any existing dismiss timer
  if (errorDismissTimer) {
    clearTimeout(errorDismissTimer);
    errorDismissTimer = null;
  }

  // Get position: centered horizontally, just above the widget
  const display = screen.getPrimaryDisplay();
  const workArea = display.workArea;
  const width = 280;
  const height = 44;
  const x = Math.round(workArea.x + (workArea.width - width) / 2);
  // Position above the widget pill. The pill sits ~8px above the dock and is at
  // most ~26px tall (recording state), so clear that region plus an 8px gap.
  const y = Math.round(workArea.y + workArea.height - 8 - 26 - 8 - height);

  if (!errorWindow || errorWindow.isDestroyed()) {
    errorWindow = new BrowserWindow({
      x,
      y,
      width,
      height,
      show: false,
      frame: false,
      transparent: true,
      hasShadow: false,
      resizable: false,
      minimizable: false,
      maximizable: false,
      fullscreenable: false,
      skipTaskbar: true,
      alwaysOnTop: true,
      movable: false,
      webPreferences: {
        preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
        nodeIntegration: false,
        contextIsolation: true,
      },
    });

    errorWindow.setAlwaysOnTop(true, "floating");
    errorWindow.setVisibleOnAllWorkspaces(true, { visibleOnFullScreen: true });
    errorWindow.loadURL(`${MAIN_WINDOW_WEBPACK_ENTRY}#error`);

    errorWindow.once("ready-to-show", () => {
      if (errorWindow) {
        errorWindow.showInactive();
        errorWindow.webContents.send("error-message", normalizedPayload);
      }
    });

    errorWindow.on("closed", () => {
      errorWindow = null;
    });
  } else {
    errorWindow.setBounds({ x, y, width, height });
    errorWindow.showInactive();
    errorWindow.webContents.send("error-message", normalizedPayload);
  }

  // Auto-dismiss after 2 seconds
  errorDismissTimer = setTimeout(() => {
    if (errorWindow && !errorWindow.isDestroyed()) {
      errorWindow.hide();
    }
    errorDismissTimer = null;
  }, 2000);
}

function sendWidgetError(payload: WidgetErrorPayload | string): void {
  showErrorBubble(payload);
}

function dismissErrorBubble(): void {
  if (errorDismissTimer) {
    clearTimeout(errorDismissTimer);
    errorDismissTimer = null;
  }
  if (errorWindow && !errorWindow.isDestroyed()) {
    errorWindow.hide();
  }
}

