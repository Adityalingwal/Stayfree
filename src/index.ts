// True when the currently visible Space is a native fullscreen-app Space, where
// the Dock is absent. Set by the space-watcher helper (see startSpaceWatcher).
// workArea is blind to this, so we track it separately and drop the widget to
// the true screen bottom in that case.
let spaceIsFullscreen = false;

// Bounds for the widget window: centered horizontally, bottom edge flush with
// the bottom of the usable area. Normally that's the work-area bottom (the dock
// top, or the screen bottom when the dock is hidden/auto-hidden — workArea
// already accounts for both). In a fullscreen Space workArea still reports the
// desktop's dock-inclusive value, so there we use the full display bottom
// instead. The pill inside is anchored to the bottom via CSS padding, so it
// visually sits ~8px above that edge and grows upward as it morphs — the native
// window itself never resizes.
function getWidgetWindowBounds(): Electron.Rectangle {
  const display = screen.getPrimaryDisplay();
  const workArea = display.workArea;
  const bounds = display.bounds;
  const x = Math.round(
    workArea.x + (workArea.width - WIDGET_WINDOW_WIDTH) / 2,
  );
  // In a fullscreen Space, sit at the true screen bottom; otherwise at the
  // work-area bottom (which sits above the dock when it's present).
  const bottom = spaceIsFullscreen
    ? bounds.y + bounds.height
    : workArea.y + workArea.height;
  const y = Math.round(bottom - WIDGET_WINDOW_HEIGHT);
  return {
    x,
    y,
    width: WIDGET_WINDOW_WIDTH,
    height: WIDGET_WINDOW_HEIGHT,
  };
}

// Wispr-style follow: when the dock is shown/hidden (or the screen changes),
// macOS's workArea changes, so the widget must slide to the new bottom edge.
// The vertical move is hand-tweened (ease-out cubic over ~250ms) rather than a
// native animated setBounds — the native animation is exactly what glitched the
// morph, so we drive it ourselves for a butter-smooth slide. X/width shifts
// (resolution changes only) are applied instantly; just the Y slides.
const WIDGET_REPOSITION_MS = 250;
let widgetTweenTimer: ReturnType<typeof setInterval> | null = null;

function repositionWidget(animate: boolean): void {
  if (!widgetWindow || widgetWindow.isDestroyed()) return;

  const target = getWidgetWindowBounds();
  const current = widgetWindow.getBounds();

  // Apply any horizontal / size change instantly (rare: resolution change).
  if (
    current.x !== target.x ||
    current.width !== target.width ||
    current.height !== target.height
  ) {
    widgetWindow.setBounds({
      x: target.x,
      y: current.y,
      width: target.width,
      height: target.height,
    });
  }

  if (widgetTweenTimer) {
    clearInterval(widgetTweenTimer);
    widgetTweenTimer = null;
  }

  const startY = widgetWindow.getBounds().y;
  const endY = target.y;
  if (startY === endY) return;

  if (!animate) {
    widgetWindow.setBounds({
      x: target.x,
      y: endY,
      width: target.width,
      height: target.height,
    });
    return;
  }

  const startTs = Date.now();
  widgetTweenTimer = setInterval(() => {
    if (!widgetWindow || widgetWindow.isDestroyed()) {
      if (widgetTweenTimer) clearInterval(widgetTweenTimer);
      widgetTweenTimer = null;
      return;
    }
    const t = Math.min(1, (Date.now() - startTs) / WIDGET_REPOSITION_MS);
    const eased = 1 - Math.pow(1 - t, 3); // ease-out cubic
    const y = Math.round(startY + (endY - startY) * eased);
    const b = widgetWindow.getBounds();
    widgetWindow.setBounds({ x: b.x, y, width: b.width, height: b.height });
    if (t >= 1) {
      if (widgetTweenTimer) clearInterval(widgetTweenTimer);
      widgetTweenTimer = null;
    }
  }, 16);
}

// --- Space watcher: detect fullscreen-app Spaces (dock absent) ---
// macOS fires no display event and workArea stays dock-inclusive when the active
// Space becomes a fullscreen app, so a tiny native helper (native/space-watcher.m)
// observes NSWorkspace space changes and reads the real Current Space type. It
// prints "1" (fullscreen) / "0" (normal) on stdout. Runs as a separate, crash-
// isolated process; if it fails we simply fall back to workArea behaviour.
let spaceWatcher: ChildProcess | null = null;

function startSpaceWatcher(): void {
  if (!isMac) return;
  // Packaged: shipped as an extraResource in Contents/Resources (outside asar).
  // Dev: copied by CopyWebpackPlugin next to the main bundle under assets/.
  const binPath = app.isPackaged
    ? path.join(process.resourcesPath, "space-watcher")
    : path.join(__dirname, "assets", "space-watcher");
  if (!fs.existsSync(binPath)) {
    console.error("[SpaceWatcher] helper not found at", binPath);
    return;
  }
  try {
    // CopyWebpackPlugin may not preserve the exec bit — ensure it's runnable.
    fs.chmodSync(binPath, 0o755);
  } catch {
    /* best-effort */
  }

  try {
    spaceWatcher = spawn(binPath, [], { stdio: ["ignore", "pipe", "pipe"] });
  } catch (err) {
    console.error("[SpaceWatcher] spawn failed:", err);
    spaceWatcher = null;
    return;
  }

  let buf = "";
  spaceWatcher.stdout?.on("data", (chunk: Buffer) => {
    buf += chunk.toString();
    let nl: number;
    while ((nl = buf.indexOf("\n")) !== -1) {
      const line = buf.slice(0, nl).trim();
      buf = buf.slice(nl + 1);
      if (line !== "0" && line !== "1") continue;
      const fullscreen = line === "1";
      if (fullscreen === spaceIsFullscreen) continue;
      spaceIsFullscreen = fullscreen;
      console.log(
        `[SpaceWatcher] space -> ${fullscreen ? "fullscreen (dock absent)" : "normal (dock present)"}`,
      );
      repositionWidget(true);
    }
  });

  spaceWatcher.stderr?.on("data", (chunk: Buffer) => {
    console.error("[SpaceWatcher]", chunk.toString().trim());
  });

  spaceWatcher.on("exit", (code, signal) => {
    console.error(`[SpaceWatcher] helper exited (code=${code} signal=${signal})`);
    spaceWatcher = null;
    // Fail-safe: leave the widget wherever it is. We do not auto-restart to
    // avoid a crash loop; the widget still follows the dock via workArea.
  });

  spaceWatcher.on("error", (err) => {
    console.error("[SpaceWatcher] process error:", err);
  });
}

function stopSpaceWatcher(): void {
  if (spaceWatcher) {
    spaceWatcher.kill();
    spaceWatcher = null;
  }
}

// An ultra-short tap records nothing meaningful — there is nothing to
// transcribe, so the widget must NOT show a "thinking" state at all: it goes
// straight from recording back to idle and the ASR round-trip is skipped.
const MIN_MEANINGFUL_RECORDING_MS = 300;

function isShortTap(): boolean {
  return (
    !activeHindiSession ||
    (activeHindiSession.stopTs ?? Date.now()) - activeHindiSession.startTs <
      MIN_MEANINGFUL_RECORDING_MS
  );
}

// Wispr-style pacing: an ultra-fast pipeline (empty audio fails ASR in ~25ms)
// would flash processing → idle within a couple of frames, which reads as a
// glitch. Hold the processing look for at least this long before going idle.
// Any newer non-idle state (a fresh recording) cancels the hold instantly.
const MIN_PROCESSING_VISIBLE_MS = 350;
let widgetProcessingSince = 0;
let widgetPendingIdleTimer: ReturnType<typeof setTimeout> | null = null;

function sendWidgetState(state: WidgetUiState): void {
  if (!widgetWindow || widgetWindow.isDestroyed()) return;

  if (widgetPendingIdleTimer) {
    clearTimeout(widgetPendingIdleTimer);
    widgetPendingIdleTimer = null;
  }

  if (state === "processing") {
    if (!widgetProcessingSince) widgetProcessingSince = Date.now();
  } else if (state === "idle" && widgetProcessingSince) {
    const shownFor = Date.now() - widgetProcessingSince;
    if (shownFor < MIN_PROCESSING_VISIBLE_MS) {
      widgetPendingIdleTimer = setTimeout(() => {
        widgetPendingIdleTimer = null;
        widgetProcessingSince = 0;
        if (widgetWindow && !widgetWindow.isDestroyed()) {
          widgetWindow.webContents.send("widget-state", "idle");
        }
      }, MIN_PROCESSING_VISIBLE_MS - shownFor);
      return;
    }
    widgetProcessingSince = 0;
  } else {
    // recording states: fresh cycle, forget the previous processing stamp
    widgetProcessingSince = 0;
  }

  // No native resize — the renderer morphs the pill purely in CSS.
  widgetWindow.webContents.send("widget-state", state);
}

