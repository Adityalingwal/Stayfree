// The floating widget lives inside ONE fixed-size, never-resized native window.
// All shape/size changes between idle/recording/processing happen in CSS inside
// this window (see widget.css) so the native frame never animates — that native
// setBounds animation was the source of the visible glitch. The window is sized
// large enough to hold the biggest pill state with a little breathing room.
const WIDGET_WINDOW_WIDTH = 260;
// The window is intentionally TALL. macOS refuses to position a window with its
// top edge below ~y=717 (it clamps it out of the dock's reserved zone), so a
// short 56px window could never sit flush at the screen bottom in a fullscreen
// Space. A tall window's top stays above that line, so its bottom edge is free
// to reach the very bottom of the screen. The pill is anchored to the window's
// bottom via CSS and only the bottom ~46px is interactive; the rest is
// transparent and click-through, so the extra height is invisible and inert.
const WIDGET_WINDOW_HEIGHT = 140;

